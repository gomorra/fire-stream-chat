@OptIn(ExperimentalMaterial3Api::class)
@Composable
internal fun SharedMediaScreen(
    onBackClick: () -> Unit,
    viewModel: SharedMediaViewModel = hiltViewModel()
) {
    val mediaUrls by viewModel.mediaUrls.collectAsState()
    var fullscreenUrl by remember { mutableStateOf<String?>(null) }

    BackHandler(enabled = fullscreenUrl != null) { fullscreenUrl = null }

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Shared Media") },
                navigationIcon = {
                    IconButton(onClick = onBackClick) {
                        Icon(Icons.AutoMirrored.Filled.ArrowBack, contentDescription = "Back")
                    }
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.background,
                    titleContentColor = MaterialTheme.colorScheme.onBackground,
                    navigationIconContentColor = MaterialTheme.colorScheme.onBackground
                )
            )
        }
    ) { padding ->
        if (mediaUrls.isEmpty()) {
            Box(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(padding),
                contentAlignment = Alignment.Center
            ) {
                Text(
                    text = "No shared media",
                    style = MaterialTheme.typography.bodyMedium,
                    color = MaterialTheme.colorScheme.onSurfaceVariant
                )
            }
        } else {
            LazyVerticalGrid(
                columns = GridCells.Fixed(3),
                contentPadding = PaddingValues(2.dp),
                horizontalArrangement = Arrangement.spacedBy(2.dp),
                verticalArrangement = Arrangement.spacedBy(2.dp),
                modifier = Modifier
                    .fillMaxSize()
                    .padding(padding)
            ) {
                items(mediaUrls) { url ->
                    AsyncImage(
                        model = url,
                        contentDescription = null,
                        contentScale = ContentScale.Crop,
                        modifier = Modifier
                            .fillMaxWidth()
                            .aspectRatio(1f)
                            .clickable { fullscreenUrl = url }
                    )
                }
            }
        }
    }

    AnimatedVisibility(visible = fullscreenUrl != null, enter = fadeIn(), exit = fadeOut()) {
        fullscreenUrl?.let { url ->
            FullscreenImageViewer(imageUrl = url, onDismiss = { fullscreenUrl = null })
        }
    }
}
